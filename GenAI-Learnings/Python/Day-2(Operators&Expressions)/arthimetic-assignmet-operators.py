# Arthimetic assignment operators
# += , -=, *=, /=, //=, %=, **=

result = 5

result +=5 # 10
print("The result is : ",result)

result -=5 # 5
print("The result is : ",result)

result *=5 # 25
print("The result is : ",result)

result /=2 # 12.5
print("The result is : ",result)

result //=2 #6.0
print("The result is : ",result)

result %=5 # 1.0
print("The result is : ",result)

result **= 5 #1.0
print("The result is : ",result)
