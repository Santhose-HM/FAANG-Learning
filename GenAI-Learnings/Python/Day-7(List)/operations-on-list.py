# Concatenation - +
l1 = [1,2,3]
l2 = [4,5,6]
l3 = l1+l2
print(l3)

# Repition - *
l3 = l1*4
print(l3)

# Membership - in,not in
if 3 in l1:
    print("Yes it is there")
if 100 not in l1:
    print("Yes it is not there")


# List Comparison - <,<=,>,>=,==,!=
if l1 == l2:
    print("l1 is = l2")
if l1>l2:
    print("l1>l2")
if l1<l2:
    print("l1<l2")
if l1>=l2:
    print("l1>=l2")
if l1<=l2:
    print("l1<=l2")
if l1!=l2:
    print("l1!=l2")

