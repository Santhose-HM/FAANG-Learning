# Condtitional Statements

# Relational Operators - They give boolean results

# <, <=, >, >=, ==, !=


a = 4
b = 5

print("The boolean result for < is : ",(a<b))
print("The boolean result for > is : ",(a>b))
print("The boolean result for <= is : ",(a<=b))
print("The boolean result for >= is : ",(a>=b))
print("The boolean result for == is : ",(a==b))
print("The boolean result for != is : ",(a!=b))



